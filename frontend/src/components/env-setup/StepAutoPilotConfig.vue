<script setup>
import { ref, computed, watch, nextTick, onMounted } from 'vue'

const props = defineProps({
  phase: {
    type: Number,
    required: true
  },
  simulationConfig: {
    type: Object,
    default: null
  },
  simulationMode: {
    type: String,
    default: 'manual'
  },
  simulationRounds: {
    type: Number,
    default: null
  }
})

const emit = defineEmits(['go-back', 'start-simulation'])

// æœ¬åœ°çŠ¶æ€
const useCustomRoundsLocal = ref(false)
const customMaxRoundsLocal = ref(40)
const stepCardRef = ref(null)

const isActive = computed(() => props.phase >= 4)
const isCompleted = computed(() => props.phase >= 4)

// å½“å‡†å¤‡å®Œæˆæ—¶ï¼Œæ»šåŠ¨åˆ°æŒ‰é’®ä½ç½®
watch(() => props.phase, async (newPhase) => {
  if (newPhase >= 4) {
    await nextTick()
    if (stepCardRef.value) {
      stepCardRef.value.scrollIntoView({ behavior: 'smooth', block: 'center' })
    }
  }
})

// ä»é…ç½®ä¸­è®¡ç®—è‡ªåŠ¨ç”Ÿæˆçš„è½®æ•°
const autoGeneratedRounds = computed(() => {
  if (!props.simulationConfig?.time_config) {
    return null
  }
  const totalHours = props.simulationConfig.time_config.total_simulation_hours
  const minutesPerRound = props.simulationConfig.time_config.minutes_per_round
  if (!totalHours || !minutesPerRound) {
    return null
  }
  const calculatedRounds = Math.floor((totalHours * 60) / minutesPerRound)
  // ç¡®ä¿æœ€å¤§è½®æ•°ä¸å°äº40ï¼ˆæ¨èå€¼ï¼‰
  return Math.max(calculatedRounds, 40)
})

// å¤„ç†å¼€å§‹æŒ‰é’®ç‚¹å‡»
const handleStart = () => {
  const params = {}
  
  if (props.simulationMode === 'auto') {
    // è‡ªåŠ¨æ¨¡å¼ä¸‹ï¼Œä¼˜å…ˆä½¿ç”¨ä¼ å…¥çš„ rounds
    params.maxRounds = props.simulationRounds || autoGeneratedRounds.value || 15
  } else if (useCustomRoundsLocal.value) {
    // ç”¨æˆ·è‡ªå®šä¹‰è½®æ•°
    params.maxRounds = customMaxRoundsLocal.value
  }
  // ä¸ä¼  maxRounds æ—¶ä½¿ç”¨è‡ªåŠ¨é…ç½®çš„è½®æ•°
  
  emit('start-simulation', params)
}

// åŒæ­¥æœ¬åœ°è‡ªå®šä¹‰è½®æ•°åˆ°åˆç†èŒƒå›´
watch(autoGeneratedRounds, (newVal) => {
  if (newVal && customMaxRoundsLocal.value > newVal) {
    customMaxRoundsLocal.value = Math.min(40, newVal)
  }
})
</script>

