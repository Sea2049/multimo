import { ref, computed } from 'vue'
import { useRouter } from 'vue-router'
import HistoryDatabase from '../components/HistoryDatabase.vue'

const router = useRouter()

// è¡¨å•æ•°æ®
const formData = ref({
  simulationRequirement: '',
  mode: 'auto', // é»˜è®¤è‡ªåŠ¨é©¾é©¶
  rounds: 15    // é»˜è®¤15è½®
})

// æ–‡ä»¶åˆ—è¡¨
const files = ref([])

// çŠ¶æ€
const loading = ref(false)
const error = ref('')
const isDragOver = ref(false)

// é«˜çº§é€‰é¡¹çŠ¶æ€
const showAdvanced = ref(false)
const enableManualMode = ref(false)

// æ–‡ä»¶è¾“å…¥å¼•ç”¨
const fileInput = ref(null)

// è®¡ç®—å±æ€§:æ˜¯å¦å¯ä»¥æäº¤
const canSubmit = computed(() => {
  return formData.value.simulationRequirement.trim() !== '' && files.value.length > 0
})

// è®¡ç®—å±æ€§:æ¨èè½®æ•°
const recommendedRounds = computed(() => {
  if (files.value.length === 0) return 15
  if (files.value.length <= 2) return 10
  if (files.value.length <= 5) return 15
  return 20
})

// è®¡ç®—å±æ€§:é¢„ä¼°æ—¶é—´
const estimatedTime = computed(() => {
  const baseTime = formData.value.rounds * 0.6
  return Math.round(baseTime)
})

// ç›‘å¬æ‰‹åŠ¨æ¨¡å¼åˆ‡æ¢
const toggleManualMode = () => {
  formData.value.mode = enableManualMode.value ? 'manual' : 'auto'
}

// è§¦å‘æ–‡ä»¶é€‰æ‹©
const triggerFileInput = () => {
  if (!loading.value) {
    fileInput.value?.click()
  }
}

// å¤„ç†æ–‡ä»¶é€‰æ‹©
const handleFileSelect = (event) => {
  const selectedFiles = Array.from(event.target.files)
  addFiles(selectedFiles)
}

// å¤„ç†æ‹–æ‹½ç›¸å…³
const handleDragOver = (e) => {
  if (!loading.value) {
    isDragOver.value = true
  }
}

const handleDragLeave = (e) => {
  isDragOver.value = false
}

const handleDrop = (e) => {
  isDragOver.value = false
  if (loading.value) return
  
  const droppedFiles = Array.from(e.dataTransfer.files)
  addFiles(droppedFiles)
}

// æ·»åŠ æ–‡ä»¶
const addFiles = (newFiles) => {
  const validFiles = newFiles.filter(file => {
    const ext = file.name.split('.').pop().toLowerCase()
    return ['pdf', 'md', 'txt'].includes(ext)
  })
  files.value.push(...validFiles)
}

// ç§»é™¤æ–‡ä»¶
const removeFile = (index) => {
  files.value.splice(index, 1)
}

// æ»šåŠ¨åˆ°åº•éƒ¨
const scrollToBottom = () => {
  window.scrollTo({
    top: document.body.scrollHeight,
    behavior: 'smooth'
  })
}

// å¼€å§‹æ¨¡æ‹Ÿ - ç«‹å³è·³è½¬ï¼ŒAPIè°ƒç”¨åœ¨Processé¡µé¢è¿›è¡Œ
const startSimulation = () => {
  if (!canSubmit.value || loading.value) return
  
  // å­˜å‚¨å¾…ä¸Šä¼ çš„æ•°æ®
  import('../store/pendingUpload.js').then(({ setPendingUpload }) => {
    setPendingUpload(files.value, formData.value.simulationRequirement, formData.value.mode)
    
    // ç«‹å³è·³è½¬åˆ°Processé¡µé¢ï¼ˆä½¿ç”¨ç‰¹æ®Šæ ‡è¯†è¡¨ç¤ºæ–°å»ºé¡¹ç›®ï¼‰
